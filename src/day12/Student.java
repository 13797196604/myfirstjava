package day12;

public class Student extends Person{
    long number;
    int math;
    int english;
    int computer;
    public Student(String name,char sex,int age,long number,int math,int english,int computer){
        this.name = name;
        this.sex = sex;
        this.age = age;
        this.number = number;
        this.math = math;
        this.english = english;
        this.computer = computer;
    }
    public double aver(){
        return aver();
    }
    public int max(){
        return max();
    }
    public int min(){
        return min();
    }
    public String toString(){
        return toString();
    }
}
